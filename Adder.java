public class Adder{

     public static void main(String []args){
        int x, y, sum;    //Declaration
        
        //Initialization
        x = 5;
        y = 10;
        sum = 0; 
        
        //Expression
        sum = x + y;
         
        //Displaying on Console
        System.out.println("Sum of x and y is: " + sum);
     }
}